package com.whjbsoft.www.vo;


public class KtxxVo {
	private Integer ftzj;
	private Integer stbh;
	private String stmc;
	private String stlx;
	private Integer ktfs;
	private String ktms;
    private Integer yksl;
    private Integer sfdt;
	public KtxxVo(Integer ftzj, Integer stbh, String stmc, String stlx, Integer ktfs, String ktms, Integer yksl, Integer sfdt) {
		super();
		this.ftzj = ftzj;
		this.stbh = stbh;
		this.stmc = stmc;
		this.stlx = stlx;
		this.ktfs = ktfs;
		this.ktms = ktms;
		this.yksl = yksl;
		this.sfdt = sfdt;
	}
	
	public KtxxVo() {
	}
	public Integer getFtzj() {
		return ftzj;
	}
	public void setFtzj(Integer ftzj) {
		this.ftzj = ftzj;
	}
	public Integer getKtfs() {
		return ktfs;
	}
	public void setKtfs(Integer ktfs) {
		this.ktfs = ktfs;
	}
	public String getKtms() {
		return ktms;
	}
	public void setKtms(String ktms) {
		this.ktms = ktms;
	}
	
	public Integer getStbh() {
		return stbh;
	}
	public void setStbh(Integer stbh) {
		this.stbh = stbh;
	}
	public String getStlx() {
		return stlx;
	}
	public void setStlx(String stlx) {
		this.stlx = stlx;
	}
	public String getStmc() {
		return stmc;
	}
	public void setStmc(String stmc) {
		this.stmc = stmc;
	}
	public Integer getYksl() {
		return yksl;
	}
	public void setYksl(Integer yksl) {
		this.yksl = yksl;
	}
	public Integer getSfdt() {
		return sfdt;
	}
	public void setSfdt(Integer sfdt) {
		this.sfdt = sfdt;
	}
	
}
