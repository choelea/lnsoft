package com.whjbsoft.www.form;

import org.apache.struts.action.ActionForm;

import com.whjbsoft.www.po.Khfk;
import com.whjbsoft.www.po.Yspsd;

public class KhfkForm extends ActionForm {
	private Khfk vo = new Khfk();
	private Yspsd yspsd = new Yspsd();
	private String fieldName;
	private String fieldValue;
	public KhfkForm() {
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public String getFieldValue() {
		return fieldValue;
	}
	public void setFieldValue(String fieldValue) {
		this.fieldValue = fieldValue;
	}
	public Khfk getVo() {
		return vo;
	}
	public void setVo(Khfk vo) {
		this.vo = vo;
	}
	public Yspsd getYspsd() {
		return yspsd;
	}
	public void setYspsd(Yspsd yspsd) {
		this.yspsd = yspsd;
	}
	
}
